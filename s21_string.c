// ------------------------------s21_memcmp----------------------------------------------
//Сравнивает первые n байтов str1 и str2
//очень похоже на strncmp, но тут abc\o\o и abc\0d - не равны, а там равны, тк
//конец строки игнорируется пока не дойдет до n
int s21_memcmp(const void *str1, const void *str2, s21_size_t n) {
  char *s1 = (char *)str1;  //указатель на первый символ 1й строки
  char *s2 = (char *)str2;  //указатель на первый символ 2й строки
  int res = 0;  //результат сравнения, к примеру (str1)a и (str2)b  = -1;
                //(str1)c и (str2)a = 2
  for (s21_size_t i = 0; i < n; i++) {
    res = *s1 - *s2;
    if (*s1 != *s2) break;  //до первого различия
    s1++;
    s2++;
  }
  return res;
}
