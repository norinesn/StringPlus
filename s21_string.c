// ------------------------------s21_strchr--------------------------------------------
//Выполняет поиск первого вхождения символа c в строке, на которую указывает
//аргумент str
// как memchr но не ограничивается байтами
char *s21_strchr(const char *str,
                 int c) {  //Выполняет поиск первого вхождения символа c в
                           //строке, на которую указывает аргумент str
  char *out = S21_NULL;  //порядковый номер

  for (s21_size_t i = 0; i <= s21_strlen(str); i++) {  // проходим всю строку
    if (str[i] == c) {
      out = ((char *)str) + i;
      break;
    }
  }

  return out;
}
