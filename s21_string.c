// ------------------------------s21_strpbrk--------------------------------------------
//Находит первый символ в строке str1, который соответствует любому символу,
//указанному в str2
char *s21_strpbrk(const char *str1, const char *str2) {
  char *out = S21_NULL;  //порядковый номер
  s21_size_t lenStr1 = s21_strlen(str1);
  s21_size_t lenStr2 = s21_strlen(str2);
  int flag = 0;
  for (s21_size_t i = 0; i < lenStr1; i++) {  // проходим всю 1ю строку
    for (s21_size_t j = 0; j < lenStr2; j++) {  // проходим всю 2ю строку
      if (str1[i] == str2[j] && flag == 0) {
        out = ((char *)str1) + i;
        flag = 1;
      }
    }
    if (flag == 1)
      break;  //в целом можно убрать, цикл пройдет до конца и покажет первое
              //совпадение
  }
  return out;
}
