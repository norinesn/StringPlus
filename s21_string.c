// ------------------------------s21_memchr----------------------------------------------
// Выполняет поиск первого вхождения символа c в первых n байтах строки, на
// которую указывает аргумент str
void *s21_memchr(const void *str, int c, s21_size_t n) {
  unsigned char *s = (unsigned char *)str;  //указатель на первый символ строки
  void *out = S21_NULL;  // совпадение не найдено
  for (s21_size_t i = 0; i < n; i++) {  //проходим указанное кол-во символов
    if (*s == (unsigned char)c) {
      out = s;  // указатель на найденный байт
      break;
    }
    s++;  //следующий символ
  }
  return out;
}
