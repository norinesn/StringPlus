// ------------------------------s21_memcpy----------------------------------------------
//Копирует n символов из src в dest = заполняет
void *s21_memcpy(void *dest, const void *src, s21_size_t n) {
  char *d = (char *)dest;  //указатель на первый символ редактируемой строки
  char *s = (char *)src;  //указатель на первый символ строки
  for (s21_size_t i = 0; i < n; i++) {
    d[i] = s[i];
  }
  return dest;
}
//Если длина строки src < n или длина строки dest <= n, то поведение будет
//неопределенным
// --------------------------------------------------------------------------------------

// ------------------------------s21_memset----------------------------------------------
//Копирует символ c в первые n символов строки, на которую указывает аргумент
// str как memcpy только все n байт меняет на 1 символ   abs (c = x, n = 3) ->
// xxx
void *s21_memset(void *str, int c, s21_size_t n) {
  char *s = (char *)str;  //указатель на первый символ строки
  for (s21_size_t i = 0; i < n; i++) {
    s[i] = c;
  }
  return str;
}
