// ------------------------------s21_strncpy----------------------------------------------
//Копирует до n символов из строки, на которую указывает src, в dest
char *s21_strncpy(char *dest, const char *src, s21_size_t n) {
  char *d = (char *)dest;  //указатель на первый символ редактируемой строки
  char *s = (char *)src;  //указатель на первый символ строки
  for (s21_size_t i = 0; i < n; i++) {
    if (s21_strlen(src) <= i)
      d[i] = '\0';
    else
      d[i] = s[i];
  }

  return dest;
}
//Если длина строки src < n, то в конец строки dest добавляются недостающие
//нулевые символы. Если же длина строки src > n, то строка dest, не будет
//заканчиваться символом конца строки.
// --------------------------------------------------------------------------------------

// ------------------------------s21_strcspn---------------------------------------------
size_t s21_strcspn(const char *str1, const char *str2) {
  int len1 = s21_strlen(str1);
  int len2 = s21_strlen(str2);
  int i;
  for (i = 0; i < len1; i++)
    for (int j = 0; j < len2; j++)
      if (str1[i] == str2[j]) return i;
  return i;
}
