// ------------------------------s21_strlen----------------------------------------------
//Вычисляет длину строки str, не включая \0
s21_size_t s21_strlen(const char *str) {
  const char *current_c;  //указатель на текущий символ
  size_t lenght = 0;      // размер строки
  for (current_c = str; *current_c != '\0'; ++current_c) {
    lenght++;
  }
  return lenght;
}
