// ------------------------------s21_strncmp--------------------------------------------
//Сравнивает не более первых n байтов str1 и str2
//очень похоже на memcmp, но тут abc\o\o и abc\0d - равны, а там нет, тк счет
//идет пока одна из строк не закончится или n
int s21_strncmp(const char *str1, const char *str2, s21_size_t n) {
  char *s1 = (char *)str1;  //указатель на первый символ 1й строки
  char *s2 = (char *)str2;  //указатель на первый символ 2й строки
  int res = 0;  //результат сравнения, к примеру (str1)a и (str2)b  = -1;
                //(str1)c и (str2)a = 2
  for (s21_size_t i = 0; i < n; i++) {
    res = *s1 - *s2;
    if (*s1 != *s2 || *s1 == '\0' || *s2 == '\0')
      break;  //до первого различия, учитывая сивол конца строки
    s1++;
    s2++;
  }
  return res;
}
