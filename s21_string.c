// ------------------------------s21_strrchr--------------------------------------------
//Выполняет поиск последнего вхождения символа c (беззнаковый тип) в строке, на
//которую указывает аргумент str.
char *s21_strrchr(const char *str, int c) {
  char *out = S21_NULL;  //порядковый номер
  for (s21_size_t i = 0; i <= s21_strlen(str); i++) {  //обработка 123\0asd\0
    if (str[i] == c)
      out = ((char *)str) + i;  // отличие от strchr - не выходим сразу, когда
                                // найдено совпадение, а ищем до конца
  }
  return out;
}
